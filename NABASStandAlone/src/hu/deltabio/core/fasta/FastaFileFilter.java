package hu.deltabio.core.fasta;

import java.io.File;
import javax.swing.filechooser.FileFilter;

/**
 *
 * @author Gábor Jaksa
 */
public class FastaFileFilter extends FileFilter {

    @Override
    public boolean accept(File f) {
        if (f.isDirectory()) {
            return true;
        }
        String path = f.getPath();
        return path.endsWith(".bz2") || path.endsWith(".fasta") || path.endsWith(".gz");
    }

    @Override
    public String getDescription() {
        return ".fasta, .gz, .bz2 files";
    }

}
