package hu.deltabio.core.io;

import com.thoughtworks.xstream.XStream;
import com.thoughtworks.xstream.io.xml.StaxDriver;
import com.thoughtworks.xstream.security.AnyTypePermission;
import com.thoughtworks.xstream.security.NoTypePermission;
import com.thoughtworks.xstream.security.NullPermission;
import com.thoughtworks.xstream.security.PrimitiveTypePermission;
import java.util.Collection;

/**
 *
 * @author Gábor Jaksa
 */
public class XmlStream extends XStream {

    public XmlStream() {
        super(new StaxDriver());
        // clear out existing permissions and set own ones
        super.addPermission(NoTypePermission.NONE);
        // allow some basics
        super.addPermission(NullPermission.NULL);
        super.addPermission(PrimitiveTypePermission.PRIMITIVES);
        super.addPermission(AnyTypePermission.ANY);
        super.allowTypeHierarchy(Collection.class);
        super.allowTypesByWildcard(new String[]{"*.*"});
    }
    
    public void toXML(Object obj, String file, SimpleFileReader.FileReaderWriterType type) {
        SimpleFileWriter w = new SimpleFileWriter(file, type);
        super.toXML(obj, w.getWriter());
        w.close();
    }

}
